r = int(input("Please enter the radius of a circle: "))
A = 3.14 * r ** 2
print("The area of the circle with a radius of " + str(r) + " is " + str(A))

l = int(input("Please enter the length of a rectangle: "))
w = int(input("Please enter the width of a rectangle: "))
A = l * w
print("The area of the rectangle with the dimensions " + str(l) + " x " + str(w) + " is " + str(A) + ".")
